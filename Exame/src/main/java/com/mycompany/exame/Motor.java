    private void MostrardatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrardatosActionPerformed
        String marcaCarro = texto1.getText();
        String añoFabricacion = Texto2.getText();
        String otrosErrores = texto3.getText();

        // Mostrar cada texto en una ventana emergente consecutiva
        JOptionPane.showMessageDialog(this, "Marca de carro: " + marcaCarro);
        JOptionPane.showMessageDialog(this, "Año de fabricación: " + añoFabricacion);
        JOptionPane.showMessageDialog(this, "Otros errores: " + otrosErrores);
    }//GEN-LAST:event_MostrardatosActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
    
        String[] datos = new String[3];
        
        String marcaCarro = texto1.getText();
        String añoFabricacion = Texto2.getText();
        String otrosErrores = texto3.getText();

        // Guardar los datos en el arreglo
        datos[0] = marcaCarro;
        datos[1] = añoFabricacion;
        datos[2] = otrosErrores;

        // Mostrar un mensaje de confirmación
        JOptionPane.showMessageDialog(this, "Los datos han sido guardados correctamente.");
    
    }//GEN-LAST:event_guardarActionPerformed

