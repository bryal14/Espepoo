    private void MostrarinformacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarinformacionActionPerformed
        // Generar un número aleatorio entre 1 y 3 para determinar el estado del motor
        int estadoAleatorio = (int) (Math.random() * 3) + 1;

        // Definir el mensaje según el estado aleatorio generado
        String mensaje;
        switch (estadoAleatorio) {
            case 1:
                mensaje = "El motor está en buen estado.";
                break;
            case 2:
                mensaje = "El motor presenta filtraciones.";
                break;
            case 3:
                mensaje = "El motor es irreparable.";
                break;
            default:
                mensaje = "No se pudo determinar el estado del motor.";
                break;
        }

        // Mostrar el mensaje emergente con la información sobre el estado del motor
        JOptionPane.showMessageDialog(this, mensaje);
    }//GEN-LAST:event_MostrarinformacionActionPerformed

